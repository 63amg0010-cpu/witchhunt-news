// 제목은 분야를 더 잘 드러내므로 2배 가중. 요약도 함께 본다.
// 키워드 '등장 횟수'를 세므로, 제목에 든 단어는 자연히 더 큰 점수를 받는다.
export function classifyCategory(title, summary, fallback) {
  const text = `${title} ${title} ${summary || ''}`
  const scores = {}
  for (const [cat, words] of Object.entries(KEYWORDS)) {
    let n = 0
    for (const w of words) n += text.split(w).length - 1
    scores[cat] = n
  }
  let best = fallback
  let bestScore = scores[fallback] ?? 0
  for (const [cat, n] of Object.entries(scores)) {
    if (n > bestScore) { bestScore = n; best = cat }
  }
  // 아무 키워드도 안 잡히면 원래 분야 유지
  return bestScore > 0 ? best : fallback
}
